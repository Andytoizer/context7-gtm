#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "../../..");
const options = parseArgs(process.argv.slice(2));
const runDir = path.join(root, options.runDir);
const manifest = readJson(path.join(runDir, "manifest.json"));
const scoutFiles = listJson(path.join(runDir, "scout-findings"));
const qaFiles = listJson(path.join(runDir, "qa-verdicts"));
const scouts = scoutFiles.map(readJson);
const qaVerdicts = qaFiles.map(readJson);
const reportPath = path.join(runDir, "report.md");

fs.writeFileSync(reportPath, renderReport());
console.log(`Wrote maintenance report to ${path.relative(root, reportPath)}`);

function parseArgs(args) {
  const parsed = { runDir: "" };
  for (let index = 0; index < args.length; index += 1) {
    const arg = args[index];
    if (arg === "--run-dir") parsed.runDir = args[++index] || "";
    else throw new Error(`Unknown argument: ${arg}`);
  }
  if (!parsed.runDir) throw new Error("--run-dir is required");
  return parsed;
}

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function listJson(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs
    .readdirSync(dir)
    .filter((file) => file.endsWith(".json"))
    .sort()
    .map((file) => path.join(dir, file));
}

function renderReport() {
  const scoutSummary = summarizeScouts(scouts);
  const qaSummary = summarizeQa(qaVerdicts);
  const missingScoutBatches = missingBatches(scouts);
  const missingQaBatches = missingBatches(qaVerdicts);
  const highSignalRows = collectHighSignalRows(qaVerdicts);

  return [
    `# Maintenance Run ${manifest.runId}`,
    "",
    "<!-- Generated by .internal/codex-maintenance/scripts/render-maintenance-report.js. -->",
    "",
    `Generated: ${manifest.generatedAt}`,
    `Tools planned: ${manifest.toolCount}`,
    `Scout batches planned: ${manifest.batchCount}`,
    `Max agents per swarm: ${manifest.maxAgentsPerSwarm}`,
    "",
    "## Status",
    "",
    table(
      ["Area", "Complete", "Partial", "Pending", "Missing"],
      [
        ["Scout", scoutSummary.complete, scoutSummary.partial, scoutSummary.pending, missingScoutBatches.length],
        ["QA", qaSummary.complete, qaSummary.partial, qaSummary.pending, missingQaBatches.length],
      ],
    ),
    "",
    "## QA Verdicts",
    "",
    table(
      ["Verdict", "Count"],
      Object.entries(qaSummary.verdictCounts)
        .sort(([a], [b]) => a.localeCompare(b, "en"))
        .map(([verdict, count]) => [verdict, count]),
    ),
    "",
    "## Missing Batches",
    "",
    missingScoutBatches.length || missingQaBatches.length
      ? table(["Type", "Batch"], [
          ...missingScoutBatches.map((batchId) => ["Scout", batchId]),
          ...missingQaBatches.map((batchId) => ["QA", batchId]),
        ])
      : "No missing batches.",
    "",
    "## High Signal Items",
    "",
    highSignalRows.length
      ? table(["Tool", "Verdict", "Notes"], highSignalRows)
      : "No approved, rejected, or human-review findings were reported.",
    "",
  ].join("\n");
}

function summarizeScouts(items) {
  return summarizeStatuses(items);
}

function summarizeQa(items) {
  const summary = summarizeStatuses(items);
  summary.verdictCounts = {};
  for (const item of items) {
    for (const verdict of item.verdicts || []) {
      summary.verdictCounts[verdict.verdict || "unknown"] = (summary.verdictCounts[verdict.verdict || "unknown"] || 0) + 1;
    }
  }
  return summary;
}

function summarizeStatuses(items) {
  return items.reduce(
    (summary, item) => {
      if (item.status === "complete") summary.complete += 1;
      else if (item.status === "partial") summary.partial += 1;
      else summary.pending += 1;
      return summary;
    },
    { complete: 0, partial: 0, pending: 0 },
  );
}

function missingBatches(items) {
  const seen = new Set(items.map((item) => item.batchId));
  return (manifest.batches || []).map((batch) => batch.batchId).filter((batchId) => !seen.has(batchId));
}

function collectHighSignalRows(items) {
  return items.flatMap((item) =>
    (item.verdicts || [])
      .filter((verdict) => !["no-change", "pending-run"].includes(verdict.verdict))
      .map((verdict) => [
        verdict.toolId || verdict.slug,
        verdict.verdict,
        verdict.qaNotes || `${(verdict.approvedFindings || []).length} approved; ${(verdict.rejectedFindings || []).length} rejected`,
      ]),
  );
}

function table(headers, rows) {
  return [
    `| ${headers.map(escapeCell).join(" | ")} |`,
    `| ${headers.map(() => "---").join(" | ")} |`,
    ...rows.map((row) => `| ${row.map(escapeCell).join(" | ")} |`),
  ].join("\n");
}

function escapeCell(value) {
  return String(value ?? "").replaceAll("|", "\\|").replaceAll("\n", " ");
}
